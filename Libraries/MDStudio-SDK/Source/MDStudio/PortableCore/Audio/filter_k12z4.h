/*************** Octave commands ****************

 function w = kaiser (n, beta)

 if (nargin < 1)
   usage ("kaiser (n, beta)");
 endif

 if (nargin < 2)
   beta = 0.5;
 endif

 if !(isscalar (n) && (n == round (n)) && (n > 0))
   error ("kaiser:  n has to be a positive integer");
 endif
 if !(isscalar (beta) && (beta == real (beta)))
   error ("kaiser:  beta has to be a real scalar");
 endif

 if (n == 1)
   w = 1;
 else
   m = n - 1;
   k = (0 : m)';
   k = 2 * beta / m * sqrt (k .* (m - k));
   w = besseli (0, k) / besseli (0, beta);
 endif

 endfunction

 x=linspace(0, 4, 8192);
 z = transpose(sinc(x)) .* kaiser(16384, 12)(8193:16384);
 csvwrite("sinc.txt", transpose(z));

************************************************/

